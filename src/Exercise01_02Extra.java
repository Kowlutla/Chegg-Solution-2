
public class Exercise01_02Extra {

	public static void main(String[] args) {

		//assign value 5.6 to v0
		double v0=5.6;
		//assign value 10.5 to v1
		double v1=10.5;
		
		//assign value 0.5 to t
		double t=0.5;
		
		//calculate average acceleration and assign to a
		double a=(v1-v0)/t;
		
		//print all variable details
		System.out.println("V0= "+v0);
		System.out.println("v1= "+v1);
		System.out.println("t= "+t);
		System.out.println("Average Acceleration= "+a);
	}
}
